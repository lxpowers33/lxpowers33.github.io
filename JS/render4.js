    function drawstroke(strokepath, start, end) {
        ctx.strokeStyle = 'rgb(80,80,80)';
        ctx.lineCap="round";
        for (var i = Math.round(strokepath.length/4*start)*4; i < Math.round(strokepath.length/4*end)*4; i = i + 4) {
            ctx.beginPath();
            ctx.moveTo(strokepath[i], strokepath[i+1]);
            ctx.lineTo(strokepath[i+4], strokepath[i+5]);
            ctx.lineWidth=170/(strokepath[i+6]+15);
            if (strokepath[i+6]<2){
                ctx.lineWidth=15;
            }  
            ctx.stroke();
            splash(strokepath[i+3]*2, strokepath[i], strokepath[i+1]);
        }
    }


    function splash(numCircles, x, y){
        for (var i = 0; i < numCircles; i++) {
            ctx.beginPath();
            if (Math.random() > 0.9) {
                ctx.arc(x-50+100*Math.random(), y-50+100*Math.random(), 1+2*Math.random(), 0, 2 * Math.PI, false);
            } else {
                ctx.arc(x-40+80*Math.random(), y-40+80*Math.random(), 1+4*Math.random(), 0, 2 * Math.PI, false);
            } 
            ctx.fillStyle = 'rgb(80,80,80)';
            ctx.fill();
        }
    }

